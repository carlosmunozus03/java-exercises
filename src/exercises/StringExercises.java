package exercises;

public class StringExercises {
    public static void main(String[] args) {
        String education = "We don't need no education\n" +
                "We don't need no thought control";
        System.out.println(education);

        System.out.println("-----------------------");

        String check = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(check);

        System.out.println("-----------------------");

        String windows = "In windows, the main driver is usually C:\\";
        System.out.println(windows);

        System.out.println("-----------------------");

        String slashes = "I can do backslashes \\, double backslashes \\\\, and the amazing triple backslash \\\\\\!";
        System.out.println(slashes);

        System.out.println("-----------------------");
    }
}
